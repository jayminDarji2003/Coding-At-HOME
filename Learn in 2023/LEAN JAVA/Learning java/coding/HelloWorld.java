    // ex. HelloWorld
    // Note :- Note that pascal convention is used for class name

// 2.Camel case convention :- In camel case convention the first character will be small and according that character other character will be capital
    // ex. helloWorld
    // Note :- Note that camel case convention is used for variable name and function(methods name).