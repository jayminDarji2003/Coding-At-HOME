class if_else_if_example{
    public static void main(String args[]){
        int num = 0;
        if(num >= 1){
            System.out.println("Number is positive");
        }
        else if(num < 0){
            System.out.println("Number is negative");
        }
        else{
            System.out.println("Number is equal to 0");
        }
    }
}